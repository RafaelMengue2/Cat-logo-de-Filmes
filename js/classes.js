class Ator{
    constructor(id, nome){
        this.nome=nome;
        this.id=id;
    }
}

class Diretor{
    constructor(id, nome){
        this.nome=nome;
        this.id=id;
    }
}

class Filme{
    constructor(id, titulo, ano, categoria, cartaz, direcao, atores, classificacao, avaliacao){
        this.id=id;
        this.titulo=titulo;
        this.ano=ano;
        this.categoria=categoria;
        this.cartaz=cartaz;
        this.direcao=direcao;
        this.atores=atores;
        this.classificacao=classificacao;
        this.avaliacao=avaliacao;    
    }
}
